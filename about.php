<h1>About Our Marketplace</h1>

<p>
  ****MARKETPLACE**** is a platform where users can browse and purchase products from different sellers in one convenient place.
</p>

<p>
  Users can explore product listings, view seller information, send offers, add items to a cart, and complete purchases through a checkout system.
</p>

<p>
  The goal of this marketplace is to provide a simple and user-friendly website where buyers and sellers can connect, compare products, and manage their marketplace activity easily.
</p>

<h2>What Users Can Do</h2>
<ul>
  <li>Browse available products</li>
  <li>View seller information</li>
  <li>Send offers on products</li>
  <li>Add items to a shopping cart</li>
  <li>Checkout and place orders</li>
  <li>Manage a user profile and order history</li>
</ul>

<h2>Why We Created This Site</h2>
<p>
  ****This website was designed for a class project. It demonstrates front-end and back-end web development using HTML, CSS, JavaScript, PHP, and MySQL. The site is intended to be responsive, easy to use, and simple to expand with more products and features in the future.
</p>